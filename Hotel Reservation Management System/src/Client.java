public class Client {

    private double nightNumber;
    private String clientName;
    private double clientAge;
    private String clientAddress;
    private double clientPhone;
    private double CreditCardNum;


    public double getNightNumber() {
        return nightNumber;
    }

    public void setNightNumber(double nightNumber) {
        this.nightNumber = nightNumber;
    }

    public String getClientName() {
        return clientName;
    }

    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

    public double getClientAge() {
        return clientAge;
    }

    public void setClientAge(double clientAge) {
        this.clientAge = clientAge;
    }

    public String getClientAddress() {
        return clientAddress;
    }

    public void setClientAddress(String clientAddress) {
        this.clientAddress = clientAddress;
    }

    public double getClientPhone() {
        return clientPhone;
    }

    public void setClientPhone(double clientPhone) {
        this.clientPhone = clientPhone;
    }

    public double getCreditCardNum() {
        return CreditCardNum;
    }

    public void setCreditCardNum(double creditCardNum) {
        CreditCardNum = creditCardNum;
    }
}
